      <p class="desc-p">
        Rio Villa Nuevo is a mineral water resort established in year 1993. It is located at Barangay Tambo M. Kulit, Indang, Cavite. The name of the resort was coined from the words “Rio”, which means “river”, and “Villa Nuevo”, which means “new village”. Generally, Rio Villa Nuevo stands for “New Village in the River”.
      </p>
      <p class="desc-p">
        With the resort’s abundance to trees and natural spring water, it has become a haven to those who seeks relaxation. The ambiance of the resort provides a tropical forest atmosphere. One of the resort’s perks is its continuously flowing fresh water supply from the mountain spring, which makes the resort different from the rest—the waters are fresh, with no chlorine added.
      </p>
      <p class="desc-p">
        Rio Villa Nuevo began as a resort with only two swimming pools conneted to the river that provides flowing water. At present, the resort is now composed of eleven swimming pools, accompanied by the growing number of amenities. The resort also offers various facilities that serve as servies and attractions to the guests.
      </p>
      <p class="desc-p">
        Rio Villa Nuevo is well-known as the resort that offers an exceptional experience to an encounter with nature, with its environmental-friendly surroundings. The guests are allowed to enjoy the sight of the animals being sheltered within the vicinity, including the natural shade brought by the lush trees. They also offer an adventurous getaway for those who love to travel outdoors through the resort’s campsite.
